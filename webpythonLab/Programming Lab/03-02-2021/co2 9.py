print("pattern using '*' \n")

a= int(input("Enter the number : "))
for i in range(0, a):
    for j in range(0, i+1):
          print("*",end="")
          
          
    print("\n")

for i in range(a,1,-1):
    
    for j in range(1, i):
          
          print("*",end="")
          
          
    print("\n")    
         


